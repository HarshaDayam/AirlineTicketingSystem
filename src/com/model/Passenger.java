package com.model;



public class Passenger {

	
	private String name;
	private int seat;
	
	public String getName() {
		return name;
	}
	public void setName(String names) {
		this.name = names;
	}
	public int getSeat() {
		return seat;
	}
	public void setSeat(int seats) {
		this.seat = seats;
	}
	
	
}
